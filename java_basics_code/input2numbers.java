import java.util.*;
public class input2numbers {
    public static void main(String[] args) {
        Scanner ip = new Scanner(System.in);
        int num1 = ip.nextInt();
        int num2 = ip.nextInt();
        int sum;
        sum = num1 + num2;
        System.out.println("Sum of two numbers is: " + sum);
    }
}
